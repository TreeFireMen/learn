package Leetcode.reverseList;

/**
 * @author zijian.zeng@hand-china.com
 * @since 2023-01-10
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
